import '@/styles/globals.css'
import type { Metadata } from 'next'
import { Inter } from 'next/font/google'
import { cn } from '@/lib/utils'
import { cookies } from 'next/headers'
import { getServerAuthSession } from '@/server/auth'
import { TRPCReactProvider } from '@/trpc/react'
import { NextAuthProvider } from '@/ctx/auth-provider'
import { ThemeProvider } from '@/ctx/theme-provider'

import GlobalStyles from '@/styles/globals'
import { Header, Footer, Main } from '@/styles/common'
import { ModeToggle } from '@/components/mode'
import Intro from './(intro)'
import Game from './(game)'
import User from '@/components/user'

const inter = Inter({
  subsets: ['latin'],
  variable: '--font-sans',
})

export const metadata: Metadata = {
  title: 'Land of Machala App',
  description: 'Generated by create-t3-app',
  icons: [{ rel: 'icon', url: './favicon.ico' }],
}

export default async function RootLayout({ children }: Readonly<{ children: React.ReactNode }>) {
  const session = await getServerAuthSession()

  return (
    <html lang='en' suppressHydrationWarning>
      <body className={cn('flex flex-col h-screen w-screen bg-background font-sans antialiased', inter.variable)}>
        <TRPCReactProvider cookies={cookies().toString()}>
          <NextAuthProvider>
            <ThemeProvider attribute='class' defaultTheme='system' enableSystem disableTransitionOnChange>
              <GlobalStyles />

              <Header>
                <div className='w-fit ml-auto space-x-1'>
                  <ModeToggle />
                  {session && <User />}
                </div>
              </Header>

              <Main>
                {!session && <Intro />}
                {session && <Game>{children}</Game>}
              </Main>

              <Footer>
                <div className='w-fit ml-auto mr-auto'></div>
              </Footer>
            </ThemeProvider>
          </NextAuthProvider>
        </TRPCReactProvider>
      </body>
    </html>
  )
}
